extern void PowerConsume_Init(void) 
{ 
    hfspi_master_init(HFSPIM_CLK_DIV_12M, HFSPI_MODE_CPOL0_CPHA1);
} 
/*******************************************************************************
* 功能描述: 
* 入口参数: 
* 出口参数: 
* 使用说明: 
*******************************************************************************/ 
extern void PowerConsume_Read(void) 
{ 
    char    RecvBuf[19+1]={0};
    hfspi_cs_low();
    hfspi_master_send_data("hello", sizeof("hello")-1);
    hfspi_master_recv_data(RecvBuf, 19);
    hfspi_cs_high();
} 
